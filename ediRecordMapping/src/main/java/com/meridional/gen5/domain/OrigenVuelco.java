package com.meridional.gen5.domain;

public class OrigenVuelco extends Origen {
	
	// Atributos
	
	private String fechaIngresoAegis;
	private String fechaEmision;
	private int ediBatchNumber;
	private int ediRecordNumber;
	private String numeroSerie;
	private String planCode;
	private String codigoCliente;
	
	// Métodos getters and setters
	
	public String getFechaIngresoAegis() {
		return fechaIngresoAegis;
	}
	public void setFechaIngresoAegis(String fechaIngresoAegis) {
		this.fechaIngresoAegis = fechaIngresoAegis;
	}
	public String getFechaEmision() {
		return fechaEmision;
	}
	public void setFechaEmision(String fechaEmision) {
		this.fechaEmision = fechaEmision;
	}
	public int getEdiBatchNumber() {
		return ediBatchNumber;
	}
	public void setEdiBatchNumber(int ediBatchNumber) {
		this.ediBatchNumber = ediBatchNumber;
	}
	public int getEdiRecordNumber() {
		return ediRecordNumber;
	}
	public void setEdiRecordNumber(int ediRecordNumber) {
		this.ediRecordNumber = ediRecordNumber;
	}
	public String getNumeroSerie() {
		return numeroSerie;
	}
	public void setNumeroSerie(String numeroSerie) {
		this.numeroSerie = numeroSerie;
	}
	public String getPlanCode() {
		return planCode;
	}
	public void setPlanCode(String planCode) {
		this.planCode = planCode;
	}
	public String getCodigoCliente() {
		return codigoCliente;
	}
	public void setCodigoCliente(String codigoCliente) {
		this.codigoCliente = codigoCliente;
	}
	
	

}
